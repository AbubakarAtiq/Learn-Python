#Dictionaries
# A dictionary is a data type similar to arrays, but works with keys
# and values instead of indexes. Each value stored in a dictionary
# can be accessed using a key, which is any type of object (a string,
# a number, a list, etc.) Instead of using its index to address it.
# For example, a database of phone numbers could be stored using a
# dictionary like this:#
phonebook={}
phonebook["abubakar"]=3219470315
phonebook["atiq"]=3096732674
phonebook["abubakar1"]=322
print(phonebook)